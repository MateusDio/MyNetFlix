    private void btnRemoverMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRemoverMouseClicked
       if (!txtIdPedido.getText().isEmpty()) {
        int idPedido = Integer.parseInt(txtIdPedido.getText());

        PedidosDTO editDTO = new PedidosDTO();
        editDTO.setId_pedido(idPedido);

        PedidosDAO editDAO = new PedidosDAO();
        editDAO.deletar(editDTO);
    } else {
        JOptionPane.showMessageDialog(null, "Informe o ID do pedido para remover.");
    }
    }//GEN-LAST:event_btnRemoverMouseClicked

    private void btnEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditarMouseClicked
         if (!txtIdPedido.getText().isEmpty()) {
        int idPedido = Integer.parseInt(txtIdPedido.getText());

        PedidosDTO editDTO = new PedidosDTO();
        editDTO.setId_pedido(idPedido);
        editDTO.setId_cliente(Integer.parseInt(txtCliente.getText()));
        editDTO.setData_pedido(txtData.getText());
        editDTO.setValor_total(Double.parseDouble(txtValor.getText()));

        PedidosDAO editDAO = new PedidosDAO();
        editDAO.editar(editDTO);
    } else {
        JOptionPane.showMessageDialog(null, "Informe o ID do pedido para editar.");
    }
       
    }//GEN-LAST:event_btnEditarMouseClicked

